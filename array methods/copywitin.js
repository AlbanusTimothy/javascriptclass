// Copies a section of the array to another position in the same array.
// Does NOT change array length.
// array.copyWithin(target, start, end)
[1, 2, 3, 4].copyWithin(0, 2);
// copies elements starting from index 2 → [3, 4, 3, 4]
// target → where to paste

// start → where to start copying

// end → where to stop copying (NOT included)